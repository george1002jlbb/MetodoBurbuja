    private void txtNumKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtNumKeyPressed
        // TODO add your handling code here:
        if (evt.getKeyCode() == KeyEvent.VK_ENTER) {
            if (!txtNum.getText().equals("")) {
                int num = Integer.parseInt(txtNum.getText());
                Numero n = new Numero(num);
                list_model.addNumero(n);
                txtNum.setText("");
                txtNum.requestFocus();
            } else {
                JOptionPane.showMessageDialog(this, "Debe digitar un numero", "ERROR", JOptionPane.ERROR_MESSAGE);
                txtNum.requestFocus();
            }
        }
    }//GEN-LAST:event_txtNumKeyPressed

